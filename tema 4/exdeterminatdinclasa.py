def hex_to_bin(hex_val):
    return format(hex_val, '08b')

# matricile pe biti
matrices = []
for hex_row in hex_sequences:
    matrix = []
    for hex_val in hex_row:
        matrix.append(hex_to_bin(hex_val))
    matrices.append(matrix)
#afisez pe orizontala
with open('matrici.txt', 'w') as f:
    for row in range(8):  # 8 linii
        line = []
        for matrix in matrices:
            line.append(matrix[row]) 
        f.write(' '.join(line) + '\n') 
